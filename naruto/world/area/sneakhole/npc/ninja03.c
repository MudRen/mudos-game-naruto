void do_fight()
{
    object enemy;
    // 沒有敵人
    enemy = this_object()->query_opponent();
    if( !enemy ) return;
    // 敵人在不同位置
    if( environment(this_object())->is_area() ) {
        if( !area_environment(this_object(), enemy) ) return;
    } else {
        if( environment(enemy) != environment(this_object()) ) return;
    }
    if( this_object()->query_stat("hp") > 501 ) {
        return;
    }
    message_vision("\n$N兩手結印蓋住傷口「"HIM"音療之術"NOR"」部份查克拉由毛細孔噴出，發出宏亮的音樂！\n"NOR, this_object());
    if( query_temp("music_heal") > 3 ) {
        message_vision("$N使盡全力卻已經無力再次治療受傷的傷口。\n\n", this_object());
        return;
    }
    this_object()->heal_stat("hp", 2000);

    if( !query_temp("music_heal") ) set_temp("music_heal", 1);
    else add_temp("music_heal", 1);

    message_vision("$N的傷口在宏亮的音樂中快速回復，居然連一點疤都看不見了！\n\n", this_object());
    return;
}
